package Table;

public class CTHD {
	private int maHD;
	private int maSach;
	private int soLuong;
	private double thanhTien;
	
	public CTHD() {
		maHD = 0;
		maSach = 0;
		soLuong = 0;
		thanhTien = 0;
	}
	
	public int getMaHD() {
		return maHD;
	}
	public void setMaHD(int maHD) {
		this.maHD = maHD;
	}
	public int getMaSach() {
		return maSach;
	}
	public void setMaSach(int maSach) {
		this.maSach = maSach;
	}
	public int getSoLuong() {
		return soLuong;
	}
	public void setSoLuong(int soLuong) {
		this.soLuong = soLuong;
	}
	public double getThanhTien() {
		return thanhTien;
	}
	public void setThanhTien(double d) {
		this.thanhTien = d;
	}
	
}
